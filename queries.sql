SELECT ce.emp_no,
	ce.first_name,
	ce.last_name,
	d.dept_name
-- INTO dept_info
FROM current_emp as ce
INNER JOIN dept_emp AS de
ON (ce.emp_no = de.emp_no)
INNER JOIN departments AS d
ON (de.dept_no = d.dept_no);

SELECT di.emp_no,
	di.first_name,
	di.last_name,
	di.dept_name
INTO dev_sales_info
FROM dept_info as di
WHERE di.dept_name IN ('Sales', 'Development');
